    public void QuebraEndereco(String end){
        StringTokenizer st = new StringTokenizer(end, ",");
        int cont=0;
        while(st.hasMoreTokens()){
            if(cont==0){
                jTextField3.setText(st.nextToken());
                cont++;
            }
            else if(cont==1){
                jTextField5.setText(st.nextToken());
                cont++;
            }
            else if(cont==2){
                jTextField6.setText(st.nextToken());
                cont++;
            }
        }
    }
    
    TelaEditarCliente(Pessoa pessoa){
        this.pessoa = pessoa;
        initComponents();
        jTextField1.setText(this.pessoa.getNome());
        jTextField2.setText(this.pessoa.getCpf());
        if(this.pessoa.getSexo().equals("Masculino")){
            jRadioButton1.setEnabled(true);
        }
        else if(this.pessoa.getSexo().equals("Feminino")){
            jRadioButton2.setEnabled(true);
        }
        //QuebraEndereco(this.pessoa.getEnd());
        jTextField4.setText(this.pessoa.getEmail());
    }

