    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Pessoa pessoa1 = new Pessoa();
        pessoa1.setCod(pessoa.getCod());
        pessoa1.setNome(jTextField1.getText());
        if(jRadioButton1.isEnabled()){
            pessoa1.setSexo("Masculino");
        }
        else if(jRadioButton2.isEnabled()){
            pessoa1.setSexo("Feminino");
        }
        pessoa1.setCpf(jTextField2.getText());
        pessoa1.setEnd(jTextField3.getText()+", "+jTextField5.getText()+" ,"+jTextField6.getText());
        pessoa1.setEmail(jTextField4.getText());
        pessoa1.setTel(jTextField7.getText());
        pessoaDAO.alterar(pessoa1);
        JOptionPane.showMessageDialog(frame, "Cliente alterado com sucesso!");
        TelaPrincipal p = new TelaPrincipal();
        p.setTitle("Tela Principal");
        p.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_jButton1ActionPerformed

